// Declarar 2 variables con número e imprimir por consola (mediante console.log()) el valor de:
// Suma
// Resta
// Multiplicación
// División

// Define variables
let x = 10;  // Asignamos un valor a x, 10
let y = 5;   // Asignamos un valor a y, 5

// Print sum, subtract, multiply and divide
console.log('Suma:', x + y);           // Imprime la suma de x e y
console.log('Resta:', x - y);          // Imprime la resta de x menos y
console.log('Multiplicación:', x * y); // Imprime la multiplicación de x por y
console.log('División:', x / y);       // Imprime la división de x entre y
